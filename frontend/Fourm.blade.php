<script>
   
   document.addEventListener("DOMContentLoaded", function() {
    const upvoteIcons = document.querySelectorAll(".upvote i.bi.bi-hand-thumbs-up-fill");
    const downvoteIcons = document.querySelectorAll(".downvote i.bi.bi-hand-thumbs-down-fill");

    // Function to toggle filled class
    function toggleFilled(event) {
        event.preventDefault();
        const icon = event.target.closest("i");
        icon.classList.toggle("filled");
    }

    // Add event listener to upvote icons
    upvoteIcons.forEach(function(icon) {
        icon.addEventListener("click", toggleFilled);
    });

    // Add event listener to downvote icons
    downvoteIcons.forEach(function(icon) {
        icon.addEventListener("click", toggleFilled);
    });
});

   
